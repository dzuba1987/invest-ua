#!/usr/bin/env node
// Regenerate changelog.js from `CHANGELOG:` lines in git commit messages.
//
// Commit convention (any commit whose subject or body contains a line matching):
//
//   CHANGELOG: <title>
//   CHANGELOG: <title> | <body>
//
// …contributes one entry. `|` separates short title from longer body.
// If no `|`, title is used as body. Emojis in title are encouraged.
//
// The script also bumps the service-worker cache version (invest-calc-vNN) whenever
// changelog.js content changes so clients fetch the fresh file on next load.
//
// Exit codes:
//   0 — nothing to do (no changes)
//   2 — files were regenerated (hook uses this to auto-commit)

import { execSync } from 'node:child_process';
import { readFileSync, writeFileSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');
const CHANGELOG_PATH = join(ROOT, 'changelog.js');
const SW_PATH = join(ROOT, 'sw.js');

function git(args) {
  return execSync(`git ${args}`, { cwd: ROOT, encoding: 'utf8' });
}

// Collect commit hashes in chronological order (oldest → newest).
const shas = git('log --reverse --format=%H --no-merges')
  .trim()
  .split('\n')
  .filter(Boolean);

const entries = [];
const seenIds = new Set();

for (const sha of shas) {
  // %aI = author date ISO-8601 strict, %B = full raw body including subject
  const out = git(`show --no-patch --format=%aI%n--__SEP__--%n%B ${sha}`);
  const [isoLine, ...rest] = out.split('\n--__SEP__--\n');
  const iso = isoLine.trim();
  const msg = rest.join('\n--__SEP__--\n');

  let perCommitIdx = 0;
  for (const line of msg.split('\n')) {
    const m = line.match(/^\s*CHANGELOG:\s*(.+?)\s*$/i);
    if (!m) continue;
    const raw = m[1].trim();
    if (!raw) continue;
    const [titleRaw, ...bodyParts] = raw.split('|');
    const title = titleRaw.trim();
    const body = bodyParts.length ? bodyParts.join('|').trim() : title;
    // Append index so multiple CHANGELOG lines in one commit each get a unique id.
    const suffix = perCommitIdx === 0 ? '' : `-${perCommitIdx}`;
    const id = `cl-${sha.slice(0, 10)}${suffix}`;
    perCommitIdx += 1;
    if (seenIds.has(id)) continue;
    seenIds.add(id);
    entries.push({ id, title, createdAt: iso, body });
  }
}

// Newest first so the Updates tab shows the latest on top.
entries.sort((a, b) => String(b.createdAt).localeCompare(String(a.createdAt)));

const header = `// AUTO-GENERATED by scripts/build-changelog.mjs.
// Do not edit by hand — rewrite on next push.
//
// To add an entry, include a line in a commit message:
//   CHANGELOG: 🔍 short title | optional longer sentence for the body
//
`;
const body = `window.BUILT_IN_CHANGELOG = ${JSON.stringify(entries, null, 2)};\n`;
const next = header + body;

const current = existsSync(CHANGELOG_PATH) ? readFileSync(CHANGELOG_PATH, 'utf8') : '';
const changelogChanged = current !== next;

let swChanged = false;
if (changelogChanged) {
  writeFileSync(CHANGELOG_PATH, next);
  // Bump the SW cache version so the new file is fetched instead of served stale.
  const sw = readFileSync(SW_PATH, 'utf8');
  const m = sw.match(/invest-calc-v(\d+)/);
  if (m) {
    const nextVer = parseInt(m[1], 10) + 1;
    const swNext = sw.replace(/invest-calc-v\d+/, `invest-calc-v${nextVer}`);
    if (swNext !== sw) {
      writeFileSync(SW_PATH, swNext);
      swChanged = true;
    }
  }
}

if (changelogChanged) {
  const swNote = swChanged ? ', bumped sw.js' : '';
  process.stdout.write(`changelog.js regenerated with ${entries.length} entries${swNote}\n`);
  process.exit(2);
}

process.stdout.write(`changelog.js already up to date (${entries.length} entries)\n`);
process.exit(0);
